package infosys.com.day2.datatypes;

/*     char and int are data type
 *     alpha and num are local variables to the method main
 *     'a' is char constant value
 *     10 is an int constant value
 *      = is an assignment operator
 *      num=10;
 *      The value which is RHS is assigned to the variable declared in LHS
 *      we can have multiple variables of different or same type in a method
 */
public class Demo6 {

	public static void main(String[] args) {
		char alpha='a';
		int num=10;
		System.out.println(alpha);
		System.out.println(num);
	}

}
